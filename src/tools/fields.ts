import { ToolContext, ToolResult, ok } from './context.js';
import { toErrorResponse } from '../errors.js';
import { FieldDefinition } from '../types/smartsuite.js';
import { proseMirrorToText } from '../utils/prosemirror.js';

/**
 * Field help text is stored as a ProseMirror doc in `params.help_doc` (the legacy
 * `help_text` key is almost always null). Always emit both keys so absence is
 * unambiguous: helpText is null when there is none.
 */
export function helpTextOf(field: FieldDefinition): { helpText: string | null; helpTextFormat: string | null } {
  const fromDoc = proseMirrorToText(field.params.help_doc);
  const legacy = field.params.help_text ?? '';
  const text = (fromDoc || legacy).trim();
  return {
    helpText: text || null,
    helpTextFormat: field.params.help_text_display_format ?? null,
  };
}

/** Per-field-type extras relevant to schema scoring (record-title, autonumber, linked, AI). */
function typeSpecific(field: FieldDefinition): Record<string, unknown> {
  const p = field.params;
  const out: Record<string, unknown> = {};

  if (field.field_type === 'recordtitlefield') {
    out['isAutoGenerated'] = p.is_auto_generated ?? false;
    if (p.template) out['template'] = p.template;
  }

  if (field.field_type === 'autonumberfield') {
    out['autoNumber'] = {
      prefix: p.prefix ?? '',
      suffix: p.suffix ?? '',
      startingNumber: p.starting_number ?? null,
      leadingZeros: p.leading_zeros ?? 0,
      includeLabels: p.include_labels ?? false,
    };
  }

  if (field.field_type === 'linkedrecordfield') {
    if (p.display_format) out['displayFormat'] = p.display_format;
    if (p.entries_allowed) out['entriesAllowed'] = p.entries_allowed;
    if (p.visible_fields?.length) out['visibleFields'] = p.visible_fields;
  }

  if (p.ai_agent) {
    out['aiField'] = {
      enabled: p.ai_agent.enabled ?? false,
      model: p.ai_agent.model ?? null,
      instructions: p.ai_agent.instructions ?? null,
      enableInternetSearch: p.ai_agent.enable_internet_search ?? false,
      runAutomatically: p.ai_agent.run_automatically ?? false,
    };
  }

  return out;
}

export async function handleListFields(
  args: Record<string, unknown>,
  ctx: ToolContext,
): Promise<ToolResult> {
  const applicationId = args['applicationId'] as string;
  try {
    const schema = await ctx.client.getApplicationSchema(applicationId);
    const fields = (schema.structure ?? []).map((f) => ({
      slug: f.slug,
      label: f.label,
      type: f.field_type,
      required: f.params.required ?? false,
      primary: f.params.primary ?? false,
      hidden: f.params.hidden ?? false,
      ...helpTextOf(f),
    }));
    return ok({ items: fields, count: fields.length });
  } catch (e) {
    const er = toErrorResponse(e);
    return { content: [{ type: 'text', text: JSON.stringify({ error: er }, null, 2) }], isError: true };
  }
}

export async function handleDescribeField(
  args: Record<string, unknown>,
  ctx: ToolContext,
): Promise<ToolResult> {
  const applicationId = args['applicationId'] as string;
  const fieldSlug = args['fieldSlug'] as string;
  try {
    const schema = await ctx.client.getApplicationSchema(applicationId);
    const field = (schema.structure ?? []).find((f) => f.slug === fieldSlug);
    if (!field) {
      return {
        content: [{ type: 'text', text: JSON.stringify({ error: { code: 'SMARTSUITE_NOT_FOUND', message: `Field "${fieldSlug}" not found in application ${applicationId}` } }, null, 2) }],
        isError: true,
      };
    }
    return ok({
      slug: field.slug,
      label: field.label,
      type: field.field_type,
      required: field.params.required ?? false,
      primary: field.params.primary ?? false,
      hidden: field.params.hidden ?? false,
      ...helpTextOf(field),
      ...(field.params.choices?.length
        ? { options: field.params.choices.map((c) => ({ value: c.value, label: c.label, color: c.value_color })) }
        : {}),
      ...(field.params.linked_application ? { linkedApplication: field.params.linked_application } : {}),
      ...(field.params.linked_field_slug ? { linkedFieldSlug: field.params.linked_field_slug } : {}),
      ...(field.params.max_length ? { maxLength: field.params.max_length } : {}),
      ...(field.params.new_choices_allowed !== undefined ? { newChoicesAllowed: field.params.new_choices_allowed } : {}),
      ...(field.params.formula
        ? {
            formula: field.params.formula,
            ...(field.params.is_advanced !== undefined ? { isAdvanced: field.params.is_advanced } : {}),
            ...(field.params.target_field_structure?.field_type
              ? { returnType: field.params.target_field_structure.field_type }
              : {}),
          }
        : {}),
      ...typeSpecific(field),
    });
  } catch (e) {
    const er = toErrorResponse(e);
    return { content: [{ type: 'text', text: JSON.stringify({ error: er }, null, 2) }], isError: true };
  }
}
